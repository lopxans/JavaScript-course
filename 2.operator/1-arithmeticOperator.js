/* Arithmetic Operators
| Operator            | Symbol | Example  | Result |
| ------------------- | ------ | -------- | ------ |
| Addition            | `+`    | `5 + 2`  | `7`    |
| Subtraction         | `-`    | `5 - 2`  | `3`    |
| Multiplication      | `*`    | `5 * 2`  | `10`   |
| Division            | `/`    | `5 / 2`  | `2.5`  |
| Modulus (Remainder) | `%`    | `5 % 2`  | `1`    |
| Exponentiation      | `**`   | `2 ** 3` | `8`    |    */

// Examples of Each Operation
console.log(5 + 2);   // 7
console.log(5 - 2);   // 3
console.log(5 * 2);   // 10
console.log(5 / 2);   // 2.5
console.log(5 % 2);   // 1
console.log(2 ** 3);  // 8

